
public class pregunta02 {
	
	
public static double Ballon(double i){
		
		return i*0.24;
	}

public static double Rodriguez(double i){
	
	return i*0.36;
}

public static double Butron(double i){
	
	return i*0.45;
}

public static double DaSilva(double i,double j){
	
	return (i+j)*0.20;
}

public static double Deza(double i,double j){
	
	return (i+j)*0.25;
}

public static double Ascues(double i,double j,double k,double p,double q){
	
	return 100000.0-(i+j+k+p+q);
}


	
	

}
